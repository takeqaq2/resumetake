// R32-M1: deep merge so missing keys in a locale fall back to English
// instead of rendering as undefined. Arrays are replaced wholesale (not
// deep-merged per element) to preserve locale-specific ordering.
function deepMerge(base, override) {
  const out = { ...base };
  for (const k of Object.keys(override || {})) {
    const bv = base[k];
    const ov = override[k];
    if (ov && typeof ov === 'object' && !Array.isArray(ov)
        && bv && typeof bv === 'object' && !Array.isArray(bv)) {
      out[k] = deepMerge(bv, ov);
    } else {
      out[k] = ov;
    }
  }
  return out;
}

// R50-F4: cache merged translations by lang. Without caching, every
// getTranslation(lang) call deep-merges the full translation tree (10
// languages × hundreds of keys), and each call returns a new object
// reference — causing unnecessary $effect/memo re-computation in
// components that use $derived(getTranslation(lang)).
const translationCache = {};

export function getTranslation(lang) {
  if (lang === 'en' || !translations[lang]) return translations.en;
  if (!translationCache[lang]) {
    translationCache[lang] = deepMerge(translations.en, translations[lang]);
  }
  return translationCache[lang];
}
